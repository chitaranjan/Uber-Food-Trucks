public class TruckData {

    String applicant;
    String facilityType;
    String address;
    String foodItems;
    String lat;
    String lon;
    String daysHours;

    public String getApplicant() {
        return applicant;
    }

    public String getFacilityType() {
        return facilityType;
    }

    public String getAddress() {
        return address;
    }

    public String getFoodItems() {
        return foodItems;
    }

    public String getLat() {
        return lat;
    }

    public String getLon() {
        return lon;
    }

    public String getDaysHours() {
        return daysHours;
    }
}
